document.addEventListener('keydown', function(event) {
  if (event.key === 'q') {
    document.getElementById('botaoq').classList.add('active');
    var somq = document.getElementById("s_keyq");
    somq.currentTime = 0; 
    somq.play();
  }
});

document.addEventListener('keyup', function(event) {
  if (event.key === 'q') {
    document.getElementById('botaoq').classList.remove('active');
  }
});

